inline vectortype VConjugate(vectortype a){
	v2complex aa;
	int k;
	for(k=0;k<nsse;k++){
		aa.d[2*k] = 1;
		aa.d[2*k+1] = -1;
	}
	return a*aa.v;
}	
inline vectortype V2CPower(vectortype x, int n) {
  v2complex tmp,tmp2;
  tmp.v = x;
  /*tmp.v2 = x;*/
  tmp2.c[0] = 1;
  tmp2.c[1] = 1;
  while( n ) {
    if( n & 1 ) {
	    tmp2.v = cmult(tmp2.v,tmp.v);
    }
    n >>= 1;
    tmp.v = cmult(tmp.v,tmp.v);
  }
  return tmp2.v;
}

